
public class mathematicalOperator {
	 public static void main(String[] args) {
		
		int x = 7;
		int y = 5;
		int z;
		
		//addition
		z = x + y;
		System.out.println(z);
		
		//subtraction
		z = x - y;
		System.out.println(z);
		
		//multiplication
		z = x * y;
		System.out.println(z);
		
		// division
		z = x / y;
		System.out.println(z);
		
		//simple modulus
		z = x % y;
		System.out.println(z);
		
		
		
}
}
